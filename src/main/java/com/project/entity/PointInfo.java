package com.project.entity;

public class PointInfo {
	private Integer pointId;
	private Integer pointCode;
	private String pointDescription;
	private Long pointTime;
	private Integer pointError;
	private Integer pointValid;
	
	
	public Integer getPointId() {
		return pointId;
	}
	public void setPointId(Integer pointId) {
		this.pointId = pointId;
	}
	public Integer getPointCode() {
		return pointCode;
	}
	public void setPointCode(Integer pointCode) {
		this.pointCode = pointCode;
	}
	public String getPointDescription() {
		return pointDescription;
	}
	public void setPointDescription(String pointDescription) {
		this.pointDescription = pointDescription;
	}
	public Long getPointTime() {
		return pointTime;
	}
	public void setPointTime(Long pointTime) {
		this.pointTime = pointTime;
	}
	public Integer getPointError() {
		return pointError;
	}
	public void setPointError(Integer pointError) {
		this.pointError = pointError;
	}
	public Integer getPointValid() {
		return pointValid;
	}
	public void setPointValid(Integer pointValid) {
		this.pointValid = pointValid;
	}
	
	
}
